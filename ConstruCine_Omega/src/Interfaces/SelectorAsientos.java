private void contenidoDentroBotones(int fila, int columna){
     switch(asientos.get(fila)[columna]){
            case "L":
                if(cantidadBoletos>0){
                asientos.get(fila)[columna] = "O";
                actualizarBotones();
                cantidadBoletos--;
                }else JOptionPane.showMessageDialog(null, "Exediste el numero de boletos");
                
                break;
            case "O":
                asientos.get(fila)[columna] = "L";
                actualizarBotones();
                cantidadBoletos++;
                break;
            case "R":
                JOptionPane.showMessageDialog(null,"Este asiento se encuentra reservado!");
                break;
        }
}
